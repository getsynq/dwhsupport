func (s *MetricsExecutionSuite) TestMetricsExecution_ProfileColumns() {
	s.skipIfNil()

	var columnsToProfile []*metrics.ColumnToProfile
	if s.Config.NumericField != "" {
		columnsToProfile = append(columnsToProfile, &metrics.ColumnToProfile{
			Column:        s.Config.NumericField,
			ColumnProfile: metrics.ColumnProfileNumeric,
		})
	}
	if s.Config.TextField != "" {
		columnsToProfile = append(columnsToProfile, &metrics.ColumnToProfile{
			Column:        s.Config.TextField,
			ColumnProfile: metrics.ColumnProfileString,
		})
	}
	if s.Config.TimeField != "" {
		columnsToProfile = append(columnsToProfile, &metrics.ColumnToProfile{
			Column:        s.Config.TimeField,
			ColumnProfile: metrics.ColumnProfileTime,
		})
	}
	if len(columnsToProfile) == 0 {
		s.T().Skip("No profile fields configured")
	}
	if s.Config.SegmentField == "" {
		s.T().Skip("SegmentField not configured for profiling")
	}

	dialect := s.Scrapper.SqlDialect()
	args := &metrics.MonitorArgs{
		Segmentation: []*metrics.Segmentation{{
			Expression: Identifier(s.Config.SegmentField),
			Rule:       metrics.AllSegments(),
		}},
	}

	qb, err := metrics.ProfileColumns(dialect, s.Config.TableFqn, columnsToProfile, args, nil, 100, 150)
	s.Require().NoError(err)

	sql, err := qb.ToSql(dialect)
	s.Require().NoError(err)
	s.T().Logf("Generated SQL:\n%s", sql)

	result, err := s.Scrapper.QueryCustomMetrics(s.ctx(), sql)
	if errors.Is(err, scrapper.ErrUnsupported) {
		s.T().Skip("QueryCustomMetrics unsupported")
	}
	s.Require().NoError(err)
	s.NotEmpty(result, "ProfileColumns query should return results")
}
