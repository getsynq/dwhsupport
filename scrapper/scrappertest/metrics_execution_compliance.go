// MetricsExecutionConfig provides the table and field information needed to
// generate and execute metric queries against a real database.
type MetricsExecutionConfig struct {
	// TableFqn is the fully qualified table name for the test table.
	TableFqn *TableFqnExpr

	// PartitioningField is a time/date column used for time segmentation (e.g. "created_at").
	PartitioningField string

	// SegmentField is a categorical column used for segmentation (e.g. "category").
	SegmentField string

	// NumericField is a numeric column for profiling (e.g. "price").
	NumericField string

	// TextField is a text column for profiling (e.g. "name").
	TextField string

	// TimeField is a time column for profiling (e.g. "created_at").
	TimeField string
}

// MetricsExecutionSuite validates that SQL generated by the metrics/querybuilder
// packages actually executes successfully against a real database. This catches
// dialect-specific SQL generation bugs that snapshot tests miss.
//
// Embed this in warehouse-specific integration test suites. Set Scrapper and
// Config before tests run (e.g., in SetupSuite).
type MetricsExecutionSuite struct {
	suite.Suite
	Scrapper scrapper.Scrapper
	Config   MetricsExecutionConfig
}

// Ctx returns a background context for use in SetupSuite of embedding suites.
func (s *MetricsExecutionSuite) Ctx() context.Context {
	return context.Background()
}

func (s *MetricsExecutionSuite) ctx() context.Context {
	return querycontext.WithQueryContext(context.Background(), complianceQueryContext)
}

func (s *MetricsExecutionSuite) skipIfNil() {
	if s.Scrapper == nil {
		s.T().Skip("Scrapper not set")
	}
	if s.Config.TableFqn == nil {
		s.T().Skip("TableFqn not configured")
	}
}

func (s *MetricsExecutionSuite) generateAndExecute(qb *querybuilder.QueryBuilder) {
	s.T().Helper()
	dialect := s.Scrapper.SqlDialect()

	sql, err := qb.ToSql(dialect)
	s.Require().NoError(err, "SQL generation should not error")
	s.NotEmpty(sql, "Generated SQL should not be empty")
	s.T().Logf("Generated SQL:\n%s", sql)

	result, err := s.Scrapper.QueryCustomMetrics(s.ctx(), sql)
	if errors.Is(err, scrapper.ErrUnsupported) {
		s.T().Skip("QueryCustomMetrics unsupported")
	}
	s.Require().NoError(err, "Query execution should not error")
	s.NotEmpty(result, "Query should return results")
}

func (s *MetricsExecutionSuite) TestMetricsExecution_VolumeQuery() {
	s.skipIfNil()

	qb := querybuilder.NewQueryBuilder(s.Config.TableFqn, metrics.TableVolumeMetricsCols())
	if s.Config.PartitioningField != "" {
		partitioning := &metrics.MonitorPartitioning{
			Field:    s.Config.PartitioningField,
			Interval: 24 * time.Hour,
		}
		qb = metrics.ApplyMonitorDefArgs(qb, &metrics.MonitorArgs{}, partitioning, 150)
	}

	s.generateAndExecute(qb)
}

func (s *MetricsExecutionSuite) TestMetricsExecution_VolumeWithSegment() {
	s.skipIfNil()
	if s.Config.SegmentField == "" {
		s.T().Skip("SegmentField not configured")
	}

	qb := querybuilder.NewQueryBuilder(s.Config.TableFqn, metrics.TableVolumeMetricsCols())
	args := &metrics.MonitorArgs{
		Segmentation: []*metrics.Segmentation{{
			Expression: Identifier(s.Config.SegmentField),
			Rule:       metrics.AllSegments(),
		}},
	}
	var partitioning *metrics.MonitorPartitioning
	if s.Config.PartitioningField != "" {
		partitioning = &metrics.MonitorPartitioning{
			Field:    s.Config.PartitioningField,
			Interval: 24 * time.Hour,
		}
	}
	qb = metrics.ApplyMonitorDefArgs(qb, args, partitioning, 150)

	s.generateAndExecute(qb)
}

